package ru.mirea.khudyakovma.fragmentmanagerapp;

import android.os.Bundle;
import android.view.View;
import android.view.LayoutInflater;
import android.view.ViewGroup;

import androidx.annotation.NonNull;
import androidx.annotation.Nullable;
import androidx.fragment.app.Fragment;
import androidx.lifecycle.ViewModelProvider;
import androidx.recyclerview.widget.LinearLayoutManager;
import androidx.recyclerview.widget.RecyclerView;

import java.util.Arrays;
import java.util.List;

public class CountryListFragment extends Fragment {

    private SharedViewModel vm;

    @Nullable
    @Override
    public View onCreateView(@NonNull LayoutInflater inflater,
                             @Nullable ViewGroup container,
                             @Nullable Bundle savedInstanceState) {
        return inflater.inflate(R.layout.fragment_list, container, false);
    }

    @Override
    public void onViewCreated(@NonNull View view, @Nullable Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);

        vm = new ViewModelProvider(requireActivity()).get(SharedViewModel.class);

        List<String> countries = Arrays.asList(
                "Россия",
                "Германия",
                "Япония",
                "Бразилия",
                "Канада",
                "Уганда"
        );


        RecyclerView rv = view.findViewById(R.id.rvCountries);
        rv.setLayoutManager(new LinearLayoutManager(requireContext()));
        rv.setAdapter(new SimpleStringAdapter(countries, vm::select));
    }
}
